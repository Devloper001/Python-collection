# // DEFINING FUNCTIONS //
def greet():
    print("Hi There")
    print("Welcome aboard")


greet()


#  // ARGUMENTS //
def greet(first_name, last_name):
    print(f"Hi {first_name} {last_name}")
    print("Welcome aboard")


greet("Dhrupad", "Pandya")
# here 'first_name' and 'last_name' are Parameters and 'Dhrupad' and 'Pandya' are Arguments


